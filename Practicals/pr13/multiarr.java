class multiarr 
{
    public static void main(String[] args) 
    {
        int a[][]={{1,7,6},{3,6,8},{3,4,6}}; 
        for(int i=0;i<3;i++)
        {
            for(int j=0;j<3;j++)
            {
                System.out.print(a[i][j]);
            }
            System.out.println();
        }   
    }    
}
